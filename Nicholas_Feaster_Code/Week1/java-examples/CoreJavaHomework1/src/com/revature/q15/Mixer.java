package com.revature.q15;

public class Mixer implements FourNonAdvanceds {
	public Mixer() {
		
	}
	public double addition(int a, int b) {
		double summed = a + b;
		return summed;
	}
	
	public double subtraction(int a, int b) {
		double difference = a - b;
		return difference;
	}
	
	public double mult(int a, int b) {
		double product = a * b;
		return product;
	}
	
	public double div(int a, int b) {
		double quotient = a / b;
		return quotient;
	}



}

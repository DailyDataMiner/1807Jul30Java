package com.iantimothyjohnson.assignments;

/**
 * Represents someone or something that can do work. It's an interface rather
 * than a class because it's more like a quality or property of a thing rather
 * than its essence (the class should be the essence of a thing).
 */
public interface Worker {
	/**
	 * Do some work.
	 */
	void doWork();
}

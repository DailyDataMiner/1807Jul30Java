package com.revature.challenged2;

//Encapsulation demonstration

public class CarAccessories {
	private String radio;
	private int seats;
	
	public String getRadio() {
		return radio;
	}
	public void setRadio(String radio) {
		this.radio = radio;
	}
	public int getSeats() {
		return seats;
	}
	public void setSeats(int seats) {
		this.seats = seats;
	}
	
	
}
